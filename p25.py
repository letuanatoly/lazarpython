# Среди натуральных чисел, не превышающих 10^9, найдите все числа,
# которые соответствуют маске 38?375*9 и делятся на 107 без остатка.
for x in range(3803759, 10 ** 9):
    x456 = x
    while x456 >= 1000000:
        x456 //= 10
    x12 = x456 // 10000
    x456 %= 1000

    if x % 10 == 9  and x12 == 38 and x456 == 375 and x % 107 == 0:
        print(x, x // 107)